
# Variable Example

a = 10
b = 6
c = a + b
print(c)
c = a - b
print(c)
c = a * b
print(c)
c = a / b
print(c)
c = a // b  # This is called Integer or Floor Division
print(c)
c = a ** b  # This will print a to the Power B
print(c)
c = ((a * b) - a)
print(c)

print(id(c))  # This is used to print address of Variable C

PI = 3.14  # We cannot create constant in Python

print(PI)

print(type(PI))  # This will print the Type of PI

# Assign Different values to multiple variable in one line

x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# Assign the same value to multiple variables in one line

x = y = z = "Orange"
print(x)
print(y)
print(z)

